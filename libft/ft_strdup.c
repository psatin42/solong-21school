#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	size_t	size;
	char	*copy;

	i = 0;
	size = ft_strlen(s1);
	copy = malloc(sizeof(*s1) * (size + 1));
	if (copy == 0)
		return (0);
	while (s1[i])
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = 0;
	return (copy);
}
